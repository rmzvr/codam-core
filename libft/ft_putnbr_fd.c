#include "libft.h"

void	ft_putnbr_fd(int n, int fd)
{
	char	digit;
	long	l;

	l = n;
	if (l < 0)
	{
		l = l * -1;
		write(fd, "-", 1);
	}
	if (l > 9)
	{
		ft_putnbr_fd(l / 10, fd);
	}
	digit = l % 10 + '0';
	write(fd, &digit, 1);
}
